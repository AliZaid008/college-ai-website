@app.route('/toggle_lang')
def toggle_lang():
    current = session.get('lang', 'ar')
    session['lang'] = 'en' if current == 'ar' else 'ar'
    return redirect(url_for('home'))

@app.route('/ask')
@app.route('/ask', methods=['GET', 'POST'])
def ask():
    # 1. Determine direction based on current language
    lang_code = session.get('lang', 'ar')
    current_dir = 'rtl' if lang_code == 'ar' else 'ltr'
    
    # 2. If the user clicked "Send" (POST request)
    if request.method == 'POST':
        email = request.form.get('email')
        question = request.form.get('question')
        
        # Here we just print to the black terminal window (simulating a database save)
        print(f"-------- NEW QUESTION RECEIVED --------")
        print(f"Email: {email}")
        print(f"Question: {question}")
        print(f"---------------------------------------")
        
        # Reload the page but show the "Success" message
        return render_template('ask.html', success=True, dir=current_dir)
    
    # 3. If the user just opened the page (GET request)
    return render_template('ask.html', success=False, dir=current_dir)
